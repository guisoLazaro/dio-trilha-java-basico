public class Area {

    public static double areaQuadrilateros(double baseMaior, double baseMenor,  double altura){

        return ((baseMaior + baseMenor) * altura )/ 2;
    }

    public static double areaQuadrilateros(double base, double altura) {
        return base * altura;
    }
    public static double areaQuadrilateros(double lado){
        return lado * lado;
    }


}
